#include "../includes/libft.h"

int	ft_count_word(char *s)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (s[i])
	{
		if ((i == 0 && !ft_isspace(s[i]))
			|| (i > 0 && !ft_isspace(s[i]) && ft_isspace(s[i - 1])))
			n++;
		i++;
	}
	return (n);
}
