#include "../includes/libft.h"

char	*ft_delchar_n(char *s, int n)
{
	int len;

	if (n < 0 || (len = ft_strlen(s)) == 0 || n > len)
		return (NULL);
	while (n < len)
	{
		s[n] = s[n + 1];
		n++;
	}
	return (s);
}
